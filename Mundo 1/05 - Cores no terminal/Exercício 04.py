"""Crie variáveis para as cores vermelho, verde, amarelo e reset, 
e utilize-as para imprimir mensagens diferentes."""

vermelho = "\033[31m"
verde = "\033[32m"
amarelo = "\033[33m"
reset = "\033[m"

print(f"{vermelho}Está é a cor vermelha{reset}")
print(f"{verde}Está é a cor verde{reset}")
print(f"{amarelo}Está é a cor amarela{reset}") 